    public editarGasto(String usuario) {
        initComponents();
        this.usuario = usuario;
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        //tabla.setModel(dtm);
        ArrayList<Gastos> gastos = this.c.obtenerGastos();
        for(int i=0;i<gastos.size();i++){
            model.addRow(new Object[]{
                gastos.get(i).getId_gasto(),
                gastos.get(i).getImporte(),
                gastos.get(i).getFecha_registro(),
                gastos.get(i).getFecha_pago(),
                gastos.get(i).getNumero_telefono(),
                gastos.get(i).getEmail_prov(),
                gastos.get(i).getDireccion_prov(),
                gastos.get(i).getNombre_prov()
            });
        }
        this.setResizable(false);
    }
