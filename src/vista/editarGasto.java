    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = tabla.getSelectedRow();
        if (row>=0){
            int identificador = Integer.parseInt(tabla.getModel().getValueAt(row, 0).toString());
            int importe = Integer.parseInt(tabla.getModel().getValueAt(row, 1).toString());
            String fecha_registro = tabla.getModel().getValueAt(row, 2).toString();
            String fecha_pago = tabla.getModel().getValueAt(row, 3).toString();
            String numero_telefono = tabla.getModel().getValueAt(row, 4).toString();
            String email_prov = tabla.getModel().getValueAt(row, 5).toString();
            String direccion_prov = tabla.getModel().getValueAt(row, 6).toString();
            String nombre_prov = tabla.getModel().getValueAt(row, 7).toString();

            int dialogButton = JOptionPane.YES_NO_OPTION;
            int resultado = JOptionPane.showConfirmDialog(this, "¿está seguro que desea modificar el gasto "+identificador+"?", "Modificar usuario", dialogButton);
            if(resultado == 0) {
                c.modificarGasto(identificador, importe, fecha_registro, fecha_pago, numero_telefono, email_prov, direccion_prov, nombre_prov);
                JOptionPane.showMessageDialog(this, "Gasto modificado");
            }             
        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = tabla.getSelectedRow();
        String identificador = tabla.getModel().getValueAt(row, 0).toString();
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int resultado = JOptionPane.showConfirmDialog(this, "¿está seguro que desea eliminar el gasto "+identificador+"?", "Modificar usuario", dialogButton);
        if(resultado == 0) {
            c.eliminarGasto(identificador);
            model.removeRow(tabla.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Gasto eliminado");
        }         
    }//GEN-LAST:event_jButton2ActionPerformed

