    public asociarRubro(String usuario) {
        initComponents();
        this.usuario = usuario;
        this.c = new contGastos();
        ArrayList<Rubro> rubros = c.obtenerRubros();
        for(int i=0;i<rubros.size();i++){
            comboRubro.addItem(rubros.get(i).getNombre());
        }
        ArrayList<Servicio> servicios = c.obtenerServiciosTabla();
        for(int i=0;i<servicios.size();i++){
            comboServicio.addItem(servicios.get(i).getActividades());
        }
        this.setResizable(false);
    }
